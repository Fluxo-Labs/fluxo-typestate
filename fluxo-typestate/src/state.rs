//! State trait and types for Fluxo Typestate.
//!
//! This module provides the core traits that form the foundation of the type-state
//! pattern in Fluxo. It includes the `State` trait that all state types must
//! implement, and the `Sealed` trait that prevents external implementations.
//!
//! # The Type-State Pattern
//!
//! The type-state pattern is a design pattern in Rust where each distinct state
//! is represented by a different type. This allows the compiler to enforce at
//! compile-time that:
//!
//! 1. Objects can only be in valid states
//! 2. State transitions can only happen through defined paths
//! 3. Methods that depend on state are only available in appropriate states
//!
//! ## Example
//!
//! Without type-state, you might have:
//!
//! ```ignore
//! struct Computer {
//!     state: State,
//!     cpu_load: f32,
//! }
//!
//! enum State { Idle, Running, Sleeping }
//!
//! impl Computer {
//!     fn start(&mut self) -> Result<(), &str> {
//!         if self.state != State::Idle {
//!             return Err("Can only start from idle");
//!         }
//!         self.state = State::Running;
//!         self.cpu_load = 0.0;
//!         Ok(())
//!     }
//! }
//! ```
//!
//! With type-state (and Fluxo), you get compile-time guarantees:
//!
//! ```ignore
//! #[state_machine]
//! enum Computer {
//!     Idle,
//!     Running { cpu_load: f32 },
//!     Sleeping,
//! }
//!
//! // This won't compile - can't call start() on Sleeping!
//! let sleeping: Computer<Sleeping> = /* ... */;
//! let running = sleeping.start(); // COMPILE ERROR!
//! ```
//!
//! # How It Works
//!
//! 1. You define your states as enum variants
//! 2. Fluxo generates a struct for each variant
//! 3. The main wrapper type is generic over the state: `Computer<S>`
//! 4. Methods are only available when the type parameter matches
//! 5. Transition methods consume `self` and return the new state type
//!
//! # State Trait
//!
//! The `State` trait is the core trait that all state types must implement.
//! It provides a single method `name()` that returns a static string
//! representing the state type name.
//!
//! The macro automatically implements this trait for each generated state struct.
//!
//! # Sealed Trait
//!
//! The `Sealed` trait is used to prevent external crates from implementing the
//! `State` trait. This is crucial for maintaining the type safety guarantees
//! of the pattern. If anyone could implement `State`, they could create states
//! that bypass the compile-time checks.
//!
//! The `Sealed` trait requires `Clone + Send + Sync + 'static` to ensure
//! states are well-behaved and can be used safely in concurrent contexts.


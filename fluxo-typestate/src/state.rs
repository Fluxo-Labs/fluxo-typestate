/// The core trait implemented by all state types in the type-state pattern.
///
/// This trait represents a single state in a state machine. Every state type
/// generated by the `#[state_machine]` macro automatically implements this trait.
///
/// # Purpose
///
/// The `State` trait serves two main purposes:
///
/// 1. **Type Marker**: It acts as a marker trait that identifies types as valid
///    states in a state machine.
///
/// 2. **Introspection**: The `name()` method allows runtime debugging by providing
///    a human-readable name for each state type.
///
/// # Implementation
///
/// **You should not implement this trait manually.** The `#[state_machine]` macro
/// automatically generates implementations for all state structs. Manual implementation
/// is prevented by the `Sealed` trait bound.
///
/// # Example
///
/// When you write:
///
/// ```ignore
/// #[state_machine]
/// enum Computer {
///     Idle,
///     Running { cpu_load: f32 },
/// }
/// ```
///
/// The macro generates:
///
/// ```ignore
/// pub struct Idle;
/// pub struct Running { pub _0: f32 }
///
/// impl fluxo_typestate::State for Idle {
///     fn name(&self) -> &'static str {
///         "Idle"
///     }
/// }
///
/// impl fluxo_typestate::State for Running {
///     fn name(&self) -> &'static str {
///         "Running"
///     }
/// }
/// ```
///
/// # Method
///
/// - `name(&self) -> &'static str`: Returns the name of the state type as a
///   static string slice. This is typically the struct name generated from
///   the enum variant name.
pub trait State: Sealed + 'static {
    /// Returns the name of this state as a static string.
    ///
    /// This method provides a way to get a human-readable representation
    /// of the current state type, useful for debugging and logging.
    ///
    /// # Returns
    ///
    /// A static string slice containing the state's name (typically the
    /// struct name generated from the enum variant).
    ///
    /// # Example
    ///
    /// ```ignore
    /// let idle = Idle;
    /// assert_eq!(idle.name(), "Idle");
    /// ```
    fn name(&self) -> &'static str;
}

/// A sealed trait that prevents external implementations of `State`.
///
/// The `Sealed` trait is a marker trait that, when combined with the `State`
/// trait, prevents downstream crates from implementing `State` for their own
/// types. This is essential for maintaining the type safety guarantees of
/// the type-state pattern.
///
/// # Why Seal the Trait?
///
/// Without sealing, anyone could implement `State` for arbitrary types:
///
/// ```ignore
/// // This would break type safety!
/// struct InvalidState;
/// impl State for InvalidState { fn name(&self) -> &str { "Invalid" } }
/// ```
///
/// By sealing the trait, we ensure that only the `#[state_machine]` macro
/// (which has access to internal implementation details) can create valid
/// state types.
///
/// # Requirements
///
/// All sealed types must be:
/// - `Clone`: Enable state cloning when needed
/// - `Send`: Enable safe transfer between threads
/// - `Sync`: Enable safe sharing between threads
/// - `'static`: Ensure the type has no lifetime dependencies
///
/// These requirements ensure states can be used safely in concurrent and
/// static contexts.
pub trait Sealed: Clone + Send + Sync + 'static {}
